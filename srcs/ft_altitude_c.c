#include "../includes/fdf.h"

void	ft_altitude_c(t_dnode **map, t_rd *rd)
{
	if ((*map)->xtab[rd->i] > 500)
		(*map)->xtab[rd->i] = 500;
	if ((*map)->xtab[rd->i] < -500)
		(*map)->xtab[rd->i] = -500;
}
